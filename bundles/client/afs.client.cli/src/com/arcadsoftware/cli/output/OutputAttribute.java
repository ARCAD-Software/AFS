package com.arcadsoftware.cli.output;

public class OutputAttribute extends AbstractOutputElement {

	
	private String value;
	
	public OutputAttribute(String name, String value) {
		super(name);
		this.value = value;
	}

	public String getValue() {
		return value;
	}
	
	public void setValue(String value) {
		this.value = value;
	}
	
}
